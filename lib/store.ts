/**
 * Простое хранилище состояния для уведомлений и глобального состояния
 * Использует паттерн Observer для реактивности
 */

// === ТИПЫ ===
export type ToastType = 'success' | 'error'

export interface Toast {
  id: string
  message: string
  type: ToastType
}

// === ГЛОБАЛЬНОЕ СОСТОЯНИЕ УВЕДОМЛЕНИЙ ===
let toasts: Toast[] = []
let listeners: Set<() => void> = new Set()

/**
 * Подписывается на изменения уведомлений
 */
export function subscribeToasts(listener: () => void) {
  listeners.add(listener)
  return () => listeners.delete(listener)
}

/**
 * Получает текущие уведомления
 */
export function getToasts(): Toast[] {
  return toasts
}

/**
 * Показывает уведомление
 */
export function showToast(message: string, type: ToastType = 'success') {
  const id = Math.random().toString(36).slice(2, 9)
  const toast: Toast = { id, message, type }
  
  toasts = [...toasts, toast]
  listeners.forEach(l => l())
  
  // Автоматически удаляем через 2.8 секунды
  setTimeout(() => {
    removeToast(id)
  }, 2800)
}

/**
 * Удаляет уведомление
 */
export function removeToast(id: string) {
  toasts = toasts.filter(t => t.id !== id)
  listeners.forEach(l => l())
}

// === ХРАНИЛИЩЕ ПЕРЕВОДОВ (localStorage) ===
const TRANSFERS_KEY = 'transfers_data_v2'

export interface TransferRecord {
  date: string
  time: string
  name: string
  dest: string
  question: string
  phone: string
}

/**
 * Получает записи переводов из localStorage
 */
export function getTransferRecords(): TransferRecord[] {
  if (typeof window === 'undefined') return []
  try {
    return JSON.parse(localStorage.getItem(TRANSFERS_KEY) || '[]')
  } catch {
    return []
  }
}

/**
 * Сохраняет запись перевода
 */
export function saveTransferRecord(record: TransferRecord) {
  const records = getTransferRecords()
  records.push(record)
  localStorage.setItem(TRANSFERS_KEY, JSON.stringify(records))
}

/**
 * Очищает все записи переводов
 */
export function clearTransferRecords() {
  localStorage.removeItem(TRANSFERS_KEY)
}

